<template>
  <div class="container">
    <div class="row">
      <div class="col-12">
        <form ref="textareaform" @submit.prevent="form" class="container">
          <div class="row">
            <div class="col mx-auto login-box">
              <div class="input-box">
                <div>
                  <label class="control-label">Select your private key (key.p12)</label>
                  <file-selector
                    v-model="this.$store.state.walletInfo.keystorePath"
                    :placeholder="this.$store.state.walletInfo.keystorePath"
                    action="SelectFile"
                  />
                </div>
                <div>
                  <fg-input
                    style="margin-bottom: 0.125em"
                    type="text"
                    v-model="alias"
                    :placeholder="this.$store.state.walletInfo.alias"
                    label="Key Alias"
                  />
                </div>
                <div>
                  <password-input
                    v-model="keystorePassword"
                    label="Keystore Password"
                    :validate=false
                  />
                </div>
                <div>
                  <password-input
                    v-model="KeyPassword"
                    label="Key Password"
                    :validate=false
                  />
                </div>
              </div>
              <div class="button-box">
                <div class="container">
                  <div class="row">
                    <div class="col">
                      <p-button
                        type="primary"
                        block
                        @click.native="login()"
                      >
                        <span style="display: block;"> LOGIN</span>
                      </p-button>
                    </div>
                  </div>
                  <div class="row">
                    <div class="col">
                      <p class="text-right">Don't have a wallet yet? Create one <a href="javascript:void(0)" @click="newWallet()">here!</a></p>
                    </div>
                  </div>
                </div>
              </div>
            </div>
          </div>
        </form>
      </div>
    </div>
    <page-overlay text="Loading..." :isActive="overlay" />
  </div>
</template>

<script>
export default {
  name: "login-screen",
  data: () => ({
    alias: "",
    keystorePassword: "",
    KeyPassword: "",
    overlay: false
  }),
  methods: {
    newWallet: function() {
      this.resetData();
      this.$router.push({
        name: 'new wallet', 
        params: {message: "Create a new Molly wallet. Please ensure that you backup all information provided below in a safe place."}
      });
    },
    resetData: function() {
      this.alias = "";
      this.KeyPassword = "";
      this.keystorePassword = "";
      this.$store.state.walletInfo.keystorePath = "";
      this.$store.state.walletInfo.alias = "";
      this.$store.state.walletInfo.keystorePassword = "";
      this.$store.state.walletInfo.KeyPassword = "";
      this.$store.state.displayLoginError = false;
    },
    login: function() {
      var self = this;
      self.$Progress.start();
      self.overlay = true;
      window.backend.WalletApplication.Login(
        self.$store.state.walletInfo.keystorePath,
        self.keystorePassword,
        self.KeyPassword,
        self.alias
      ).then(result => {
        if (result) {
          window.backend.WalletApplication.SetUserTheme().then(
            darkMode => (self.$store.state.walletInfo.darkMode = darkMode)
          )
          window.backend.WalletApplication.SetWalletTag().then(
            walletTag => (self.$store.state.walletInfo.email = walletTag)
          );
          window.backend.WalletApplication.SetImagePath().then(
            imagePath => (self.$store.state.walletInfo.imgPath = imagePath)
          );
          self.overlay = false;
          self.$Progress.finish();
          self.$store.state.app.isLoggedIn = true;
          self.$router.push({
            name: 'loading', 
            params: {message: "Getting your $DAG Wallet ready..."}
          });
        } else {
          self.overlay = false;
          self.$Progress.fail();
        }
      });
    }
  }
};
</script>

